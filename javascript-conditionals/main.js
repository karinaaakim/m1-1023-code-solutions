function categorizeAcidity(pH) {
  if (pH === 7) {
    return 'neutral';
  }
  if (pH > 0 && pH < 7) {
    return 'acid';
  }
  if (pH < 14 && pH > 7) {
    return 'base';
  } else {
    return 'invalid pH level';
  }
}

